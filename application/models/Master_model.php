    }
    function update_postal($id,$data){
        return $this->db->where('id',$id)->update('postal_charge', $data);
    }

    function get_calendarData($start,$end){
        $this->db->select('*')->where('gregdate >=',$start)->where('gregdate <',$end);
        return $this->db->order_by('id','asc')->get('calendar_malayalam')->result();
    }

    function get_english_month($date){
        return $this->db->select('*')->where('gregdate',$date)->get('calendar_malayalam')->row_array();
